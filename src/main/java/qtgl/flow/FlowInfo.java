package qtgl.flow;


public class FlowInfo {


	private String Workflows_GUID;
	private String Workflows_ID;
	private String Workflows_Code;
	private String Pid;

	public String getWorkflows_GUID() {
		return Workflows_GUID;
	}
	public void setWorkflows_GUID(String workflows_GUID) {
		Workflows_GUID = workflows_GUID;
	}
	public String getWorkflows_ID() {
		return Workflows_ID;
	}
	public void setWorkflows_ID(String workflows_ID) {
		Workflows_ID = workflows_ID;
	}
	public String getWorkflows_Code() {
		return Workflows_Code;
	}
	public void setWorkflows_Code(String workflows_Code) {
		Workflows_Code = workflows_Code;
	}
	public String getPid() {
		return Pid;
	}
	public void setPid(String pid) {
		Pid = pid;
	}

	 
 
	
}
